class Ques07{
   public static void main(String args[])
    {
        // bits Representation
        System.out.println("Number of Bits ="+Byte.SIZE);

        // Byte Representation
        System.out.println("Number of Bytes ="+Byte.BYTES);

        // Minimum Value of Byte
        System.out.println("Number of Bits ="+Byte.MIN_VALUE);

        // Maximum Value of Byte
        System.out.println("Number of Bits ="+Byte.MAX_VALUE);
    }
}